const defaultLang = 'fr';
const langStorageKey = 'quiztempo_lang';

const getMessage = (lang, key) => translations[lang]?.[key] ?? translations[defaultLang]?.[key] ?? '';

const applyLanguage = (inputLang) => {
  const lang = translations[inputLang] ? inputLang : defaultLang;

  document.documentElement.lang = lang;
  document.title = getMessage(lang, 'meta.title');

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.dataset.i18n;
    const value = getMessage(lang, key);
    if (value) node.textContent = value;
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((node) => {
    const pairs = node.dataset.i18nAttr.split(';').map((chunk) => chunk.trim()).filter(Boolean);
    pairs.forEach((pair) => {
      const [attr, key] = pair.split(':').map((part) => part.trim());
      const value = getMessage(lang, key);
      if (attr && key && value) node.setAttribute(attr, value);
    });
  });

  document.querySelectorAll('[data-lang]').forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle('active', isActive);
    button.setAttribute('aria-pressed', isActive ? 'true' : 'false');
  });

  try {
    localStorage.setItem(langStorageKey, lang);
  } catch (_) {
    // Ignore storage issues.
  }

  return lang;
};

const resolveInitialLanguage = () => {
  try {
    const stored = localStorage.getItem(langStorageKey);
    if (stored && translations[stored]) return stored;
  } catch (_) {
    // Ignore storage issues.
  }

  const browserLang = (navigator.language || '').toLowerCase();
  if (browserLang.startsWith('en')) return 'en';
  return defaultLang;
};

const selectedLang = applyLanguage(resolveInitialLanguage());

document.querySelectorAll('[data-lang]').forEach((button) => {
  button.addEventListener('click', () => {
    const lang = button.dataset.lang;
    if (!lang || !translations[lang]) return;
    if (document.documentElement.lang === lang) return;
    applyLanguage(lang);
  });
});

const header = document.querySelector('.site-header');
const nav = document.querySelector('#site-nav');
const toggle = document.querySelector('.nav-toggle');

if (toggle && nav) {
  toggle.addEventListener('click', () => {
    const isOpen = nav.classList.toggle('open');
    toggle.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
  });

  nav.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      nav.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    });
  });
}

const updateHeader = () => {
  if (!header) return;
  header.classList.toggle('scrolled', window.scrollY > 10);
};

updateHeader();
window.addEventListener('scroll', updateHeader, { passive: true });

const revealNodes = document.querySelectorAll('.reveal');
if (revealNodes.length > 0 && 'IntersectionObserver' in window) {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('in');
        observer.unobserve(entry.target);
      }
    });
  }, {
    threshold: 0.2,
  });

  revealNodes.forEach((node) => observer.observe(node));
} else {
  revealNodes.forEach((node) => node.classList.add('in'));
}

const track = document.getElementById('screens-track');
const prev = document.getElementById('slide-prev');
const next = document.getElementById('slide-next');

if (track && prev && next) {
  const getStep = () => {
    const firstCard = track.querySelector('img');
    if (!firstCard) return 240;
    const cardWidth = firstCard.getBoundingClientRect().width;
    return Math.round(cardWidth + 14);
  };

  prev.addEventListener('click', () => {
    track.scrollBy({ left: -getStep() * 2, behavior: 'smooth' });
  });

  next.addEventListener('click', () => {
    track.scrollBy({ left: getStep() * 2, behavior: 'smooth' });
  });
}

